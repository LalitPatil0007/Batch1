package controlStatementsFlows;

public class RightAngledTriangleStarPattern {

	public static void main(String[] args) {

		int dimension = 10;
		
		for(int row = 1 ; row <= dimension ; row++) {
			
			
			for(int col = 1 ; col <= row ; col++) {
				System.out.print("* ");
			}
			
			System.out.println();
			
			
			
			
		}
	}

}
